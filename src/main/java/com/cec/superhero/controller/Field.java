package com.cec.superhero.controller;

/**
 *
 * @author etdeh
 */
public class Field {
    private String name;
    private String value;
    private String size;
    private String type;

    public Field(String name, String value, String size, String type) {
        this.name = name;
        this.value = value;
        this.size = size;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    
        
}
